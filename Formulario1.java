           clientetxt.setText(""); telefonotxt.setText(""); direcciontxt.setText("");
           edadtxt.setText("0"); sueldotxt.setText(""); 
           clientetxt.requestFocus();
           jButton1.setEnabled(true);
           jButton2.setEnabled(false);
           jButton3.setEnabled(false);
